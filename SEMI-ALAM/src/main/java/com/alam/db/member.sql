CREATE TABLE MEMBER(
	MEMBER_ID VARCHAR2(30) NOT NULL PRIMARY KEY,
	MEMBER_PW VARCHAR2(1000) NOT NULL,
	MEMBER_SALT VARCHAR2(1000) NOT NULL,
	MEMBER_NAME VARCHAR2(15),
	MEMBER_EMAIL VARCHAR2(300), 
	MEMBER_PHONE VARCHAR2(15),
	MEMBER_ADDRESS VARCHAR2(1000),
	MEMBER_CATEGORY NUMBER,
	MEMBER_GENDER VARCHAR2(5) CHECK (MEMBER_GENDER IN ('F', 'M')),
	MEMBER_GRADE VARCHAR2(5) DEFAULT 'B', 
	CONSTRAINT MEMBER_CATEGORY_FK FOREIGN KEY(MEMBER_CATEGORY) REFERENCES CATEGORY(CATEGORY_NUM),
	CONSTRAINT MEMBER_GRADE_CHK CHECK (MEMBER_GRADE IN ('B', 'A', 'S'))
);

