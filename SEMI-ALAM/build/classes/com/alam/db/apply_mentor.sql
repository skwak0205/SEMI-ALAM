DROP SEQUENCE APPLY_NO;
DROP TABLE APPLY_MENTOR;

CREATE SEQUENCE APPLY_NO;

CREATE TABLE APPLY_MENTOR(
	APPLY_NO NUMBER NOT NULL PRIMARY KEY,
	APPLY_CATEGORY NUMBER NOT NULL REFERENCES CATEGORY(CATEGORY_NUM),
	APPLY_ID VARCHAR2(30) NOT NULL REFERENCES MEMBER(MEMBER_ID) ON DELETE CASCADE,
	APPLY_PRICE NUMBER NOT NULL,
	APPLY_CONTENT VARCHAR2(3000) NOT NULL,
	APPLY_HOW VARCHAR2(30) NOT NULL,
	APPLY_DATE DATE NOT NULL
);


select * from apply_mentor;